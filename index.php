		<p>Very much embarrassed, after having vainly exhausted our philological resources, I knew not what part to take, when Conseil said: What country is it? My two companions stretched themselves on the cabin carpet, and were soon sound asleep. For my own part, too many thoughts crowded my brain, too many insoluble questions pressed upon me, too many fancies kept my eyes half open. Where were we? What strange power carried us on? I felt-or rather fancied I felt-the machine sinking down to the lowest beds of the sea. Dreadful nightmares beset me; I saw in these mysterious asylums a world of unknown animals, among which this submarine boat seemed to be of the same kind, living, moving, and formidable as they. Then my brain grew calmer, my imagination wandered into vague unconsciousness, and I soon fell into a deep sleep. I think that we are caught, captain.</p>
