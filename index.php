		<p>Well, captain, but now we come to the real difficulty. I can understand your rising to the surface; but diving below the surface, does not your submarine contrivance encounter a pressure, and consequently undergo an upward thrust of one atmosphere for every thirty feet of water, just about fifteen pounds per square inch? Look. Perhaps in China; you know the Nautilus is a rapid traveler. It goes through water as swallows through the air, or as an express on the land. It does not fear frequented seas; who can say that it may not beat the coasts of France, England, or America, on which flight may be attempted as advantageously as here. The portion of the terrestrial globe which is covered by water is estimated at upward of eighty millions of acres. This fluid mass comprises two billions two hundred and fifty millions of cubic miles, forming a spherical body of a diameter of sixty leagues, the weight of which would be three quintillions of tons. To comprehend the meaning of these figures, it is necessary to observe that a quintillion is to a billion as a billion is to unity; in other words, there are as many billions in a quintillion as there are units in a billion. This mass of fluid is equal to about the quantity of water which would be discharged by all the rivers of the earth in forty thousand years.</p>
