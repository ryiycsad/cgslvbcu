		<p>Forty-two degrees. I think that, in the excess of his joy, the Canadian, if he had not talked so much, would have killed them all. But he contented himself with a single dozen of these interesting marsupians. These animals were small. They were a species of those kangaroo rabbits that live habitually in the hollows of trees, and whose speed is extreme; but they are moderately fat, and furnish, at least, estimable food. We were very satisfied with the results of the hunt. Happy Ned proposed to return to this enchanting island the next day, for he wished to depopulate it of all the eatable quadrupeds. But he reckoned without his host. And these musicians? said I, pointing out some works of Weber, Rossini, Mozart, Beethoven, Haydn, Meyerbeer, Herold, Wagner, Auber, Gounod, and a number of others scattered over a large model piano organ which occupied one of the panels of the drawing-room. All the day of the 22d of February we passed in the Sargasso Sea, where such fish as are partial to marine plants and fuci find abundant nourishment. The next, the ocean had returned to its accustomed aspect. From this time for nineteen days, from the 23d of February to the 12th of March, the Nautilus kept in the middle of the Atlantic, carrying us at a constant speed of a hundred leagues in twenty-four hours. Captain Nemo evidently intended accomplishing his submarine programme, and I imagined that he intended, after doubling Cape Horn, to return to the Australian seas of the Pacific. Ned Land had cause for fear. In these large seas, void of islands, we could not attempt to leave the boat. Nor had we any means of opposing Captain Nemo's will. Our only course was to submit; but what we could neither gain by force nor cunning, I liked to think might be obtained by persuasion. This voyage ended, would he not consent to restore our liberty, under an oath never to reveal his existence-an oath of honor which we should have religiously kept? But we must consider that delicate question with the captain. But was I free to claim this liberty? Had he not himself said from the beginning, in the firmest manner, that the secret of his life exacted from him our lasting imprisonment on board the Nautilus? And would not my four months' silence appear to him a tacit acceptance of our situation? And would not a return to the subject result in raising suspicions which might be hurtful to our projects if at some future time a favorable opportunity offered to return to them?</p>
